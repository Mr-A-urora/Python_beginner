# 算数运算符

a = 5
b = 2
# 加法
c = a + b
print(c)
# 减法
d = a - b
print(d)
# 乘法
e = a * b
print(e)
# 除法
f = a / b
print(f)
# 整除
g = a // b
print(g)
# 幂
h = a ** b
print(h)
# 取余
i = a % b
print(i)

